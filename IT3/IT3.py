# A list of food created, containing 4 elements
foods = ["Bacon Roll","Sandwich","Soup","Noodles"]

# A for loop created that loops through each element in the list
for food in foods:
    if food == "Soup":              # If the element equals "soup"
        print("This is soup")       # Then it prints "This is soup"
    else:                           # If the element doesn't equal "soup"
        print("Food is not soup")   # Then it prints "Food is not soup"
